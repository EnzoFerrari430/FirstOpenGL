void GPU_select_save_to_image(const char* filePath, int textureWidth, int textureHeight)
{
    FILE* output_image;
    int output_width, output_height;

    // glFramebufferTexture2D(GL_FRAMEBUFFER, GL_COLOR_ATTACHMENT0, GL_TEXTURE_2D, texture, 0);

    output_width = textureWidth;
    output_height = textureHeight;

    /// READ THE PIXELS VALUES from FBO AND SAVE TO A .PPM FILE
    int x, y, k;
    unsigned char* r_buf = (unsigned char*)malloc(output_width * output_height * 4);

    GLint dataFormat;
    glGetIntegerv(GL_IMPLEMENTATION_COLOR_READ_FORMAT, &dataFormat);

    GLint dataType;
    glGetIntegerv(GL_IMPLEMENTATION_COLOR_READ_TYPE, &dataType);
    /// READ THE CONTENT FROM THE FBO
    glReadBuffer(GL_COLOR_ATTACHMENT0);
    glReadPixels(0, 0, output_width, output_height, GL_RGBA, GL_UNSIGNED_BYTE, r_buf);

    output_image = fopen(filePath, "wt");
    fprintf(output_image, "P3\n");
    fprintf(output_image, "# Created by Ricao\n");
    fprintf(output_image, "%d %d\n", output_width, output_height);
    fprintf(output_image, "255\n");

    k = 0;
    for (y = 0; y < output_height; y++) {
        for (x = 0; x < output_width; x++) {

            unsigned char* p = &r_buf[y * output_width + 4 * x];
            unsigned char r = *p; p++;
            unsigned char g = *p; p++;
            unsigned char b = *p;
            fprintf(output_image, "%u %u %u ", r, g, b);
        }
        fprintf(output_image, "\n");
    }
    fclose(output_image); // 关闭文件
    free(r_buf);
}

void screenShot()
{
    int color_width = SCREEN_WIDTH;
    int color_height = SCREEN_HEIGHT;
    int color_x = 0;
    int color_y = 0;
    unsigned char* bytes = (unsigned char*)malloc(SCREEN_WIDTH * SCREEN_HEIGHT * 4);
    glReadPixels(color_x, color_y, color_width, color_height, GL_RGBA, GL_UNSIGNED_BYTE, bytes);
    GLenum err = glGetError();

    stbi_flip_vertically_on_write(1);
    stbi_write_png("background.png", color_width, color_height, 4, bytes, SCREEN_WIDTH * 4);

    free(bytes);
}

