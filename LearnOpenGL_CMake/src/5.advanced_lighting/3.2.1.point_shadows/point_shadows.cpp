// ppm
#include "tgaimage.h"
void GPU_select_save_to_image(const char* filePath, unsigned int textureID , int width, int height)
{
    FILE* output_image = NULL;
    int output_width, output_height;

    output_width = width;
    output_height = height;

    int bytespp = 4;

    /// READ THE PIXELS VALUES from FBO AND SAVE TO A .PPM FILE
    int x, y, k;
    unsigned char* r_buf = (unsigned char*)malloc(output_width * output_height * 4);

    glBindTexture(GL_TEXTURE_2D, textureID);
    GLenum error = glGetError();
    if (error != GL_NO_ERROR)
    {
        std::cout << "GL ERROR when glBindTexture" << std::endl;
    }
    glGetTexImage(GL_TEXTURE_2D, 0, GL_RGBA, GL_UNSIGNED_BYTE, r_buf);
    error = glGetError();
    if (error != GL_NO_ERROR)
    {
        std::cout << "GL ERROR when glGetTexImage" << std::endl;
    }

    unsigned char developer_area_ref[4] = { 0, 0, 0, 0 };
    unsigned char extension_area_ref[4] = { 0, 0, 0, 0 };
    unsigned char footer[18] = { 'T', 'R','U', 'E', 'V', 'I', 'S', 'I', 'O', 'N','-', 'X', 'F', 'I', 'L', 'E', '.', '\0' };

    std::ofstream out;
    out.open(filePath, std::ios::binary);
    if (!out.is_open())
    {
        std::cerr << "can't open file " << filePath << "\n";
        out.close();
        return;
    }
    TGA_Header header;
    memset(&header, 0, sizeof(TGA_Header));
    header.bitsperpixel = bytespp << 3;
    header.width = width;
    header.height = height;
    header.datatypecode = 2;
    header.imagedescriptor = 0x20; // top-left origin
    out.write((char*)&header, sizeof(TGA_Header));
    if (!out.good())
    {
        out.close();
        std::cerr << "can't dump the tga file\n";
        return;
    }

    out.write((char*)r_buf, width * height * bytespp);
    if (!out.good())
    {
        std::cerr << "can't unload raw data\n";
        out.close();
        return;
    }


    out.write((char*)developer_area_ref, sizeof(developer_area_ref));
    if (!out.good())
    {
        std::cerr << "can't dump the tga file\n";
        out.close();
        return;
    }

    out.write((char*)extension_area_ref, sizeof(extension_area_ref));
    if (!out.good())
    {
        std::cerr << "can't dump the tga file\n";
        out.close();
        return;
    }

    out.write((char*)footer, sizeof(footer));
    if (!out.good())
    {
        std::cerr << "can't dump the tga file\n";
        out.close();
        return;
    }
    out.close();
    free(r_buf);
}
